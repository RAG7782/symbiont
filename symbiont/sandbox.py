class DockerSandbox(Sandbox):
    """
    Sandbox backed by Docker containers for full OS-level isolation.

    Uses Docker-on-Docker (DooD) pattern: the host Docker socket is NOT
    mounted by default. Each execute() spins up a fresh container, runs
    the command, and removes it (--rm). Workspace files are bind-mounted.

    Prerequisites: Docker daemon accessible to the running process.

    Usage:
        sandbox = DockerSandbox(
            sandbox_id="sb:t1",
            thread_id="t1",
            config=SandboxConfig(docker_image="python:3.12-slim"),
        )
        out, code = await sandbox.execute("python3 /mnt/workspace/app.py")
    """

    def __init__(
        self,
        sandbox_id: str,
        thread_id: str,
        config: SandboxConfig,
        skills_dir: str | None = None,
    ) -> None:
        self.id = sandbox_id
        self.thread_id = thread_id
        self._config = config

        base = Path(config.base_dir).expanduser().resolve()
        self._workspace = base / _safe_thread_dirname(thread_id)
        self._workspace.mkdir(parents=True, exist_ok=True)

        self._skills_dir = Path(skills_dir).expanduser().resolve() if skills_dir else None
        self._image = config.docker_image
        self._network = config.docker_network

    def _docker_run_args(self, command: str) -> list[str]:
        args = [
            "docker", "run", "--rm",
            f"--network={self._network}",
            "--memory=512m",
            "--cpus=1",
            f"-v", f"{self._workspace}:/mnt/workspace",
        ]
        if self._skills_dir and self._skills_dir.exists():
            args += ["-v", f"{self._skills_dir}:/mnt/skills:ro"]
        args += [
            "-w", "/mnt/workspace",
            self._image,
            "sh", "-c", command,
        ]
        return args

    async def execute(self, command: str, timeout: int | None = None) -> tuple[str, int]:
        lines: list[str] = []
        code = 0
        async for line in self.execute_stream(command, timeout=timeout):
            lines.append(line)
        if lines and lines[-1].startswith("\x00EXIT:"):
            try:
                code = int(lines[-1][6:])
            except ValueError:
                code = -1
            lines = lines[:-1]
        raw = "".join(lines)
        if len(raw) > self._config.bash_output_max_chars:
            raw = raw[: self._config.bash_output_max_chars] + "\n[... truncated]"
        return raw, code

    async def execute_stream(
        self, command: str, timeout: int | None = None
    ) -> AsyncIterator[str]:
        args = self._docker_run_args(command)
        effective_timeout = timeout or self._config.command_timeout_sec
        proc = await asyncio.create_subprocess_exec(
            *args,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.STDOUT,
        )
        try:
            async with asyncio.timeout(effective_timeout):
                async for line in _read_stream(proc.stdout):
                    yield line
        except TimeoutError:
            proc.kill()
            yield f"[docker timeout after {effective_timeout}s]\n"
            yield "\x00EXIT:124"
            return
        await proc.wait()
        yield f"\x00EXIT:{proc.returncode}"

    async def read_file(self, container_path: str) -> str:
        host = self._workspace / container_path.removeprefix("/mnt/workspace/")
        def _read() -> str:
            return host.read_text(errors="replace")
        content = await asyncio.to_thread(_read)
        if len(content) > self._config.read_file_max_chars:
            content = content[: self._config.read_file_max_chars] + "\n[... truncated]"
        return content

    async def write_file(self, container_path: str, content: bytes) -> None:
        host = self._workspace / container_path.removeprefix("/mnt/workspace/")
        host.parent.mkdir(parents=True, exist_ok=True)
        await asyncio.to_thread(host.write_bytes, content)

    async def list_dir(self, container_path: str, max_depth: int = 2) -> tuple[list[dict], bool]:
        out, _ = await self.execute(f"find /mnt/workspace -maxdepth {max_depth} -print")
        entries = [{"path": line.strip(), "type": "unknown"} for line in out.splitlines() if line.strip()]
        return entries[:250], len(entries) > 250

    async def glob(self, pattern: str) -> tuple[list[str], bool]:
        out, _ = await self.execute(f"find /mnt/workspace -name '{Path(pattern).name}'")
        matches = [l.strip() for l in out.splitlines() if l.strip()]
        return matches[:500], len(matches) > 500

    async def grep(self, pattern: str, paths: list[str]) -> tuple[list[dict], bool]:
        paths_str = " ".join(f'"{p}"' for p in paths)
        out, _ = await self.execute(f"grep -rn '{pattern}' {paths_str} 2>/dev/null")
        results = []
        for line in out.splitlines():
            parts = line.split(":", 2)
            if len(parts) >= 3:
                try:
                    results.append({"file": parts[0], "line": int(parts[1]), "content": parts[2]})
                except ValueError:
                    continue
        return results[:200], len(results) > 200


