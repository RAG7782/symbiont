class SandboxProvider:
    """
    Factory and lifecycle manager for Sandbox instances.

    backend="local"  → LocalSandbox  (default)
    backend="docker" → DockerSandbox (requires Docker daemon)
    """

    def __init__(
        self,
        config: SandboxConfig | None = None,
        skills_dir: str | None = None,
        backend: str = "local",
    ) -> None:
        self._config = config or SandboxConfig()
        self._skills_dir = skills_dir
        self._backend = backend
        self._sandboxes: dict[str, Sandbox] = {}

    def acquire(self, thread_id: str) -> str:
        """Return sandbox_id for thread_id, creating if needed."""
        if thread_id not in self._sandboxes:
            sandbox_id = f"sb:{thread_id}"
            if self._backend == "docker":
                sb: Sandbox = DockerSandbox(
                    sandbox_id=sandbox_id, thread_id=thread_id,
                    config=self._config, skills_dir=self._skills_dir,
                )
            else:
                sb = LocalSandbox(
                    sandbox_id=sandbox_id, thread_id=thread_id,
                    config=self._config, skills_dir=self._skills_dir,
                )
            self._sandboxes[thread_id] = sb
            logger.info("sandbox: acquired %s [%s] for thread %s", sandbox_id, self._backend, thread_id)
        return self._sandboxes[thread_id].id

    def get(self, sandbox_id: str) -> Sandbox | None:
        for sb in self._sandboxes.values():
            if sb.id == sandbox_id:
                return sb
        return None

    def get_by_thread(self, thread_id: str) -> Sandbox | None:
        return self._sandboxes.get(thread_id)

    def release(self, thread_id: str) -> None:
        sb = self._sandboxes.pop(thread_id, None)
        if sb:
            workspace = Path(self._config.base_dir).expanduser() / _safe_thread_dirname(thread_id)
            try:
                shutil.rmtree(workspace, ignore_errors=True)
                logger.info("sandbox: released %s, workspace cleaned", sb.id)
            except Exception as exc:
                logger.warning("sandbox: cleanup failed for %s: %s", sb.id, exc)

    def shutdown(self) -> None:
        for tid in list(self._sandboxes):
            self.release(tid)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _safe_thread_dirname(thread_id: str) -> str:
    """Sanitize thread_id for use as a directory name."""
    return thread_id.replace("/", "_").replace(":", "_").replace("..", "_")


async def _read_stream(stream: StreamReader) -> AsyncIterator[str]:
    while True:
        line = await stream.readline()
        if not line:
            break
        yield line.decode(errors="replace")


# ---------------------------------------------------------------------------
# Module-level singleton
# ---------------------------------------------------------------------------

_provider: SandboxProvider | None = None


def get_sandbox_provider(
    config: SandboxConfig | None = None,
    skills_dir: str | None = None,
    backend: str = "local",
) -> SandboxProvider:
    global _provider
    if _provider is None:
        _provider = SandboxProvider(config=config, skills_dir=skills_dir, backend=backend)
    return _provider
